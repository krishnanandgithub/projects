/* WAP to find the squire root of a number.
   Take the number as input from the users.
   res=Math.sqrt(n);
*/


import java.util.*;

class task1_3
{

public static void main(String[] args)

{
  Scanner s=new Scanner(System.in);

  System.out.print("Enter a number to find the square root:");
  
  double r=s.nextDouble();

  double res=Math.sqrt(r);
  
System.out.println("Square root of "+r+" is : "+res);
 
}


}





